    for (int k = 0; k < p; k++) { d(i, 0) = -1.0; d(i++, 1) =  1.0; } // edge 2
    for (int k = 0; k < p; k++) { d(i, 0) =  0.0; d(i++, 1) = -1.0; } // edge 3

